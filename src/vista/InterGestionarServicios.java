    private void CargarTablaServicios() {
        Connection con = Conexion.conectar();
        DefaultTableModel model = new DefaultTableModel();
        String sql = "select IDSERVICIOS, NOMBRE, APELLIDO, DNI, DIRECCION, TELEFONO, ESTADO, Stdo from tb_servicio";
        Statement st;
        try {
            st = con.createStatement();
            ResultSet rs = st.executeQuery(sql);
            InterGestionarServicios.jTable_Servicios = new JTable(model);
            InterGestionarServicios.jScrollPane1.setViewportView(InterGestionarServicios.jTable_Servicios);

            model.addColumn("N°");//ID
            model.addColumn("NOMBRE");
            model.addColumn("APELLIDO");
            model.addColumn("DNI");
            model.addColumn("DIRECCION");
            model.addColumn("TELEFONO");
            model.addColumn("ESTADO");
            model.addColumn("Stdo");

            while (rs.next()) {


                Object fila[] = new Object[8];
                for (int i = 0; i < 8; i++) {
                    fila[i] = rs.getObject(i + 1);
                }
                model.addRow(fila);
            }
            con.close();
        } catch (SQLException e) {
            System.out.println("Error al llenar la tabla Servicios: " + e);
        }
        //evento para obtener campo al cual el usuario da click
        //y obtener la interfaz que mostrara la informacion general
        jTable_Servicios.addMouseListener(new MouseAdapter() {
            @Override
            public void mouseClicked(MouseEvent e) {
                int fila_point = jTable_Servicios.rowAtPoint(e.getPoint());
                int columna_point = 0;

                if (fila_point > -1) {
                    IDSERVICIOS = (int) model.getValueAt(fila_point, columna_point);
                    EnviarDatosServicioSeleccionado(IDSERVICIOS);//metodo
                }
            }
        });
    }

    private void EnviarDatosServicioSeleccionado(int IDSERVICIOS) {
        try {
            Connection con = Conexion.conectar();
            PreparedStatement pst = con.prepareStatement(
                    "select * from tb_servicio where IDSERVICIOS = '" + IDSERVICIOS + "'");
            ResultSet rs = pst.executeQuery();
            if (rs.next()) {
                txt_NOMBRE.setText(rs.getString("NOMBRE"));
                txt_APELLIDO.setText(rs.getString("APELLIDO"));
                txt_DNI.setText(rs.getString("DNI"));
                txt_TELEFONO.setText(rs.getString("TELEFONO"));
                txt_DIRECCION.setText(rs.getString("DIRECCION"));
                txt_ESTADO.setText(rs.getString("ESTADO"));
            }
            con.close();
        } catch (SQLException e) {
            System.out.println("Error al seleccionar Servicios: " + e);
        }
    }
    
