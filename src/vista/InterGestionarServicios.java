    private void jButton_actualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton_actualizarActionPerformed
        Servicios servicios = new Servicios();
        Ctrl_Servicios controlServicios = new Ctrl_Servicios();
        if (txt_NOMBRE.getText().equals("") || txt_APELLIDO.getText().equals("") || txt_DNI.getText().equals("")
                || txt_DIRECCION.getText().equals("") || txt_TELEFONO.getText().equals("") || txt_ESTADO.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "Complete todos los campos");
        } else {
            try {
                servicios.setNOMBRE(txt_NOMBRE.getText().trim());
                servicios.setAPELLIDO(txt_APELLIDO.getText().trim());
                servicios.setDNI(txt_DNI.getText().trim());
                servicios.setDIRECCION(txt_DIRECCION.getText().trim());
                servicios.setTELEFONO(txt_TELEFONO.getText().trim());
                servicios.setESTADO(txt_ESTADO.getText().trim());
                servicios.setStdo(1);

                if (controlServicios.actualizar(servicios,IDSERVICIOS)) {
                    JOptionPane.showMessageDialog(null, "Registro Actualizado");
                    //CARGARServicios
                    this.CargarTablaServicios();
                    this.Limpiar();
                } else {
                    JOptionPane.showMessageDialog(null, "Error al Actualizar");
                }

            } catch (HeadlessException | NumberFormatException e) {
                System.out.println("Error en: " + e);
            }
        }
    }//GEN-LAST:event_jButton_actualizarActionPerformed

