    private void Login(){
        if(!txt_usuario.getText().isEmpty() && !txt_password.getText().isEmpty()){
            Controlador_usuario controlUsuario = new Controlador_usuario();
            Usuario usuario = new Usuario();
            usuario.setUsuario(txt_usuario.getText().trim());
            usuario.setPassword(txt_password.getText().trim());
            Menu m=new Menu();
            m.setVisible(true);
            dispose();
            if(controlUsuario.loginUser(usuario)){
                JOptionPane.showMessageDialog(null, "Bienvenido al sistema de inventario "+txt_usuario.getText()+"");
            }else{
                JOptionPane.showMessageDialog(null , "Usuario o clave Incorrectos");
            }
        }else{
            JOptionPane.showMessageDialog(null, "Ingresa tu identificaciÃ³n de usuario");
        }
    }

